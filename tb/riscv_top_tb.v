`timescale 1ns/1ps

module riscv_top_tb;

// ======================================================
// TESTBENCH SIGNALS
// ======================================================

reg clk;
reg rst;


// ======================================================
// DUT
// ======================================================

riscv_top dut (

    .clk(clk),
    .rst(rst)

);


// ======================================================
// CLOCK GENERATION
// ======================================================

always #5 clk = ~clk;


// ======================================================
// TEST SEQUENCE
// ======================================================

initial begin

    clk = 0;
    rst = 1;

    #20;

    rst = 0;

    #300;

    $finish;

end


// ======================================================
// MONITOR
// ======================================================

initial begin

    $monitor(

        "TIME=%0t | PC=%h | INST=%h | ALU_RESULT=%h | WB_DATA=%h",

        $time,
        dut.pc,
        dut.instruction,
        dut.alu_result,
        dut.wb_data

    );

end


// ======================================================
// WAVEFORM
// ======================================================

initial begin

    $dumpfile("riscv_top.vcd");
    $dumpvars(0, riscv_top_tb);

end

endmodule
